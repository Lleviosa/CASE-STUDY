    private void simpanPromoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanPromoActionPerformed
        // TODO add your handling code here:
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy/MM/dd");
        try {
            if (comboPromo.getSelectedItem().toString().equals("CASHBACK")) {
                String kodePromo = promoCode.getText();
                LocalDate tanggalAwal = LocalDate.parse(tglAwal.getText(), formatter);
                LocalDate tanggalAkhir = LocalDate.parse(tglAkhir.getText(), formatter);
                double persenPotongan = Double.parseDouble(persenPotong.getText());
                int maksPotongan = Integer.parseInt(maxPotong.getText());
                int minPembelian = Integer.parseInt(minBeli.getText());
                Promosi promo = new Cashback(kodePromo, tanggalAwal, tanggalAkhir, persenPotongan, maksPotongan, minPembelian);
                Main.promosiMap.put(kodePromo, promo);
            }
            if (comboPromo.getSelectedItem().toString().equals("DISKON")) {
                String kodePromo = promoCode.getText();
                LocalDate tanggalAwal = LocalDate.parse(tglAwal.getText(), formatter);
                LocalDate tanggalAkhir = LocalDate.parse(tglAkhir.getText(), formatter);
                double persenPotongan = Double.parseDouble(persenPotong.getText());
                int maksPotongan = Integer.parseInt(maxPotong.getText());
                int minPembelian = Integer.parseInt(minBeli.getText());
                Promosi promo = new Diskon(kodePromo, tanggalAwal, tanggalAkhir, persenPotongan, maksPotongan, minPembelian);
                Main.promosiMap.put(kodePromo, promo);
            }
            if (comboPromo.getSelectedItem().toString().equals("DISKON ONGKIR")) {
                String kodePromo = promoCode.getText();
                LocalDate tanggalAwal = LocalDate.parse(tglAwal.getText(), formatter);
                LocalDate tanggalAkhir = LocalDate.parse(tglAkhir.getText(), formatter);
                double persenPotongan = Double.parseDouble(persenPotong.getText());
                int maksPotongan = Integer.parseInt(maxPotong.getText());
                int minPembelian = Integer.parseInt(minBeli.getText());
                Promosi promo = new DiskonOngkir(kodePromo, tanggalAwal, tanggalAkhir, persenPotongan, maksPotongan, minPembelian);
                Main.promosiMap.put(kodePromo, promo);
            }
            loadPromo();
        } catch (Exception e) {
            System.out.println(e.getMessage());

        }

    }//GEN-LAST:event_simpanPromoActionPerformed

    private void simpanMenuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanMenuActionPerformed
        // TODO add your handling code here:
        if (comboMenu.getSelectedItem().toString().equals("DOKUMEN")) {
            String IDMenu = menuID.getText();
            String namaMenu = menuNama.getText();
            int hargaFinal = Integer.parseInt(hargaMenu.getText());
            Cetak menu = new Dokumen(IDMenu, namaMenu, hargaFinal);
            Main.cetakMap.put(IDMenu, menu);
        }
        if (comboMenu.getSelectedItem().toString().equals("FOTOKOPI")) {
            String IDMenu = menuID.getText();
            String namaMenu = menuNama.getText();
            int hargaFinal = Integer.parseInt(hargaMenu.getText());
            Cetak menu = new Fotokopi(IDMenu, namaMenu, hargaFinal);
            Main.cetakMap.put(IDMenu, menu);
        }
        loadMenu();

    }//GEN-LAST:event_simpanMenuActionPerformed

