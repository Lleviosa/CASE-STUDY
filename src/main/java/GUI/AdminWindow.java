    public void loadMember() {
        DefaultTableModel tableMember = (DefaultTableModel) memberTable.getModel();

        for (Map.Entry<String, Pelanggan> entry : userMap.entrySet()) {
            String memberID = entry.getKey();
            Pelanggan pelanggan = entry.getValue();
            Object[] rowData = {memberID, pelanggan.getNamaAwal(), ((Member) pelanggan).getJoinDate(), pelanggan.getSaldo()};
            tableMember.addRow(rowData);
        }
    }

    public void loadGuest() {
        DefaultTableModel tableGuest = (DefaultTableModel) guestTable.getModel();

        for (Map.Entry<String, Pelanggan> entry : userMap.entrySet()) {
            String ID = entry.getKey();
            Pelanggan pelanggan = entry.getValue();
            Object[] rowData = {ID, pelanggan.getSaldo()};
            tableGuest.addRow(rowData);
        }
    }

    public void loadPromo() {
        DefaultTableModel tablePromo = (DefaultTableModel) promoTable.getModel();

        for (Map.Entry<String, Promosi> entry : promosiMap.entrySet()) {
            String kodePromo = entry.getKey();
            Promosi promo = entry.getValue();
            if (promo instanceof Cashback) {
                Object[] rowData = {kodePromo, ((Cashback) promo).getTanggalAwal(), ((Cashback) promo).getTanggalAkhir(), ((Cashback) promo).getPersenPotongan(), ((Cashback) promo).getMaksPotongan(), ((Cashback) promo).getMinPembelian()};
                tablePromo.addRow(rowData);
            }
            if (promo instanceof Diskon) {
                Object[] rowData = {kodePromo, ((Diskon) promo).getTanggalAwal(), ((Diskon) promo).getTanggalAkhir(), ((Diskon) promo).getPersenPotongan(), ((Diskon) promo).getMaksPotongan(), ((Diskon) promo).getMinPembelian()};
                tablePromo.addRow(rowData);
            }
            if (promo instanceof DiskonOngkir) {
                Object[] rowData = {kodePromo, ((DiskonOngkir) promo).getTanggalAwal(), ((DiskonOngkir) promo).getTanggalAkhir(), ((DiskonOngkir) promo).getPersenPotongan(), ((DiskonOngkir) promo).getMaksPotongan(), ((DiskonOngkir) promo).getMinPembelian()};
                tablePromo.addRow(rowData);
            }
        }
    }

    public void loadMenu() {
        DefaultTableModel tableMenu = (DefaultTableModel) menuTable.getModel();

        for (Map.Entry<String, Cetak> entry : cetakMap.entrySet()) {
            String IDMenu = entry.getKey();
            Cetak menu = entry.getValue();
            Object[] rowData = {IDMenu, menu.getNama(), menu.getHargaFinal()};
            tableMenu.addRow(rowData);
        }
    }
    
